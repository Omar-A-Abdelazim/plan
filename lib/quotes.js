// Keep backward compatibility with existing code
export const MOTIVATIONAL_QUOTES = MOTIVATIONAL_QUOTES_EN

export function getRandomQuote(language = 'en') {
  const quotes = language === 'ar' ? MOTIVATIONAL_QUOTES_AR : MOTIVATIONAL_QUOTES_EN
  return quotes[Math.floor(Math.random() * quotes.length)]
}

export function getDailyQuote(language = 'en') {
  const quotes = language === 'ar' ? MOTIVATIONAL_QUOTES_AR : MOTIVATIONAL_QUOTES_EN
  // Use the date as seed for consistent daily quote
  const today = new Date()
  const seed = today.getFullYear() * 10000 + (today.getMonth() + 1) * 100 + today.getDate()
  const index = seed % quotes.length
  return quotes[index]
}
